public class informationSubject {
    private String idSubject;
    private String subject;
    private int numberTiet;
    private int numberTinchi;
    private String nameTeacher;

    public informationSubject() {
        
    }

    public String getIdSubject() {
        return idSubject;
    }

    public void setIdSubject(String idSubject) {
        this.idSubject = idSubject;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public int getNumberTiet() {
        return numberTiet;
    }

    public void setNumberTiet(int numberTiet) {
        this.numberTiet = numberTiet;
    }

    public int getNumberTinchi() {
        return numberTinchi;
    }

    public void setNumberTinchi(int numberTinchi) {
        this.numberTinchi = numberTinchi;
    }

    public String getNameTeacher() {
        return nameTeacher;
    }

    public void setNameTeacher(String nameTeacher) {
        this.nameTeacher = nameTeacher;
    }
    
}
